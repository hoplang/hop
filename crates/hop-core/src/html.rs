#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn classifies_known_html_element() {
        let el = HtmlElement::parse("div").unwrap();
        assert_eq!(el, HtmlElement::Div);
        assert_eq!(el.as_str(), "div");
    }

    #[test]
    fn classifies_known_svg_element() {
        assert_eq!(HtmlElement::parse("path"), Some(HtmlElement::Path));
    }

    #[test]
    fn classifies_hyphenated_custom_element() {
        let el = HtmlElement::parse("my-widget").unwrap();
        assert_eq!(
            el,
            HtmlElement::Custom(CheapString::new("my-widget".to_string()))
        );
        assert_eq!(el.as_str(), "my-widget");
    }

    #[test]
    fn rejects_unknown_bare_name() {
        assert_eq!(HtmlElement::parse("dvi"), None);
    }

    #[test]
    fn rejects_mathml() {
        assert_eq!(HtmlElement::parse("math"), None);
    }

    #[test]
    fn void_element_check_matches_helper() {
        assert!(HtmlElement::parse("br").unwrap().is_void());
        assert!(!HtmlElement::parse("div").unwrap().is_void());
        assert!(!HtmlElement::parse("my-widget").unwrap().is_void());
    }
}
