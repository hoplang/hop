    pub fn is_void(&self) -> bool {
        is_void_element(self.as_str())
    }

    pub fn parse(name: &str) -> Option<HtmlElement> {
        if let Some(el) = Self::known(name) {
            Some(el)
        } else if name.contains('-') {
            Some(HtmlElement::Custom(CheapString::new(name.to_string())))
        } else {
            None
        }
    }
